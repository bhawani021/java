
public class maths {
    public static void main(String[] args) {
        // max value
        int maxVal = Math.max(10, 20);
        System.out.println(maxVal);

        // min value
        int minVal = Math.min(1, 9);
        System.out.println(minVal);

        // square root
        double squareRoot = Math.sqrt(9);
        System.out.println(squareRoot);




    }
}
